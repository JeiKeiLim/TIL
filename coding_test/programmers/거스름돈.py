def solution(n: int, money: List[int]) -> int:
    modular = 1_000_000_007
    money.sort()
    dp = [0] * (n+1)
    dp[0] = 1
    for coin in money:
        for i in range(coin, n+1):
            dp[i] = (dp[i] + dp[i-coin]) % modular
    return dp[-1]


